e volviendo desde San Sebastián a Madrid donde teníamos reunión con colegas de un viaje anterior. Aquí pasamos el fin de semana conociendo y recorriendo la capi, y mejor paro aquí para no crear tiranteces que yo me arranco enseguida. xD<br />Después de la visita a Asturias la verdad que se me quedaron muchas ganas de volver. No creo que tarde, la verdad.<br />¿De qué parte de Asturias eres?<p>Estadísticas: Publicado por <a href="http://losersjuegos.com.ar/foro/memberlist.php?mode=viewprofile&amp;u=1853">Dokan</a> — Mar Abr 21, 2009 7:53 pm</p><hr />
