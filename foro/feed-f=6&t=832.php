No ese formato, cuando cargas una imagen en SDL, ya no existe eso del formato de la imagen, todas las imagenes se representan con la misma estructura, la cual es el de una superficie y lo que contiene es el array de valores, sobre las estructuras hay libros en esta web que contiene información de las estrcuturas internas de SDL, quizas no te sirva saber como trabaja SDL, pero lo que te digo lo aprendí de ese libro.<br />AL hablar del formato dentro de SDL, se refiere a como se representan los valores de la imagen, es decir cada pixel esta dado por un numero de bits, 24 bits es una imagen de color real. Existen de 16, 8, 4, y 32 bpp(bits por pixel), generalmente para las computadoras modernas se usan 32 bits, que admite la tranparencia de imagenes, asi ya no tienes que hacer el colorkey, yo uso imagenes de 32 bits con el formato PNG, y de hecho es mas rapido que usar imagenes de 24.<br />De cualquier forma si trabajas con menores bpp se debe hacer la converción al formato de la superficie, por ejemplo si tu superficie es de 32 bits, y cargas una imagen de 24 bits, deberias convertir la imagen a 32 bits. Este proceso se hace antes de copiar cada imagen a la superficie, pues la converción entre formatos requiere un tiempo considerable de computo.<p>Estadísticas: Publicado por <a href="http://losersjuegos.com.ar/foro/memberlist.php?mode=viewprofile&amp;u=2124">carlostex</a> — Vie Ago 20, 2010 2:00 am</p><hr />
