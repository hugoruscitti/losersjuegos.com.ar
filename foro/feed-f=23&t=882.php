Scratch tiene una pieza de movimiento:  <span style="font-weight: bold">(rebotar si está tocando un borde)</span>. Es decir, igual que un objeto puede moverse o girar, puede rebotar si está tocando el borde. Rebotar produce un giro de 180 grados en el objeto. Si el objeto avanzaba hacia la derecha, al rebotar avanzará hacia la izquierda.<br /><br />También tiene una condición: <span style="font-weight: bold">&lt;¿tocando borde?&gt;</span> que permite detectar que un objeto sale de la pantalla. Entonces puedes hacer que rebote (pong) o que desaparezca (proyectil). Puedes mirar la pág. 11 y 14 de la guía de referencia <a href="http://www.eduteka.org/pdfdir/ScratchGuiaReferencia.pdf" class="postlink">http://www.eduteka.org/pdfdir/ScratchGuiaReferencia.pdf</a>.<br /><br />Puedo mandar un ejemplo si quieres que quede más claro. A mi me parece interesante porque es una de las tareas que hay que programar con más frecuencia y que puede ser interesante que puedan aprender los objetos.<br /><br />-- lm<p>Estadísticas: Publicado por <a href="http://losersjuegos.com.ar/foro/memberlist.php?mode=viewprofile&amp;u=2338">lmorillas</a> — Jue Oct 14, 2010 3:38 pm</p><hr />
